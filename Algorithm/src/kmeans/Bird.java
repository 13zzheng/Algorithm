public class Bird {

    private float x=0;
    private float y=0;
    private float z=0;
    private float m=0;
    private String birdClass="";


    public float getM() {
        return m;
    }

    public void setM(float m) {
        this.m = m;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public float getZ() {
        return z;
    }

    public void setZ(float z) {
        this.z = z;
    }

    public String getBirdClass() {
        return birdClass;
    }

    public void setBirdClass(String birdClass) {
        this.birdClass = birdClass;
    }
}
